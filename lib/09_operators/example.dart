void example() {
//artimeticos
  print(10 + 10);
  print(10 - 5);
  print(10 * 2);
  print(10 / 2);
  //relationals
  print('Juan' == 'juan');
  print('Juan' != 'juan');
  print(10 > 2);
  print(10 < 2);
  print(5 >= 6);
  print(5 <= 6);
  //logical
  print(10 < 20 && 'Juan'.length == 4);
  print(10 > 20 || 'Juan'.length == 4);
  print(!true);
}
